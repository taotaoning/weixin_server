package com.nt.weixinapp.message.menu.pojo;

//普通按钮（子按钮）
public class CommonButton extends Button {

	//按钮的类型
	private String type;
	
	//按钮的key值
	private String key;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
	
	
}
